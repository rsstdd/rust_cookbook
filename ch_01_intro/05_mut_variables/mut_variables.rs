fn main() {
    println!("**\nDefining mutable variables\n** \n");

    let mut sample_var = 10; // Mutable - thus, we can change it
    println!("Value of the sample variable is {}", sample_var);

    let sample_var = 20;
    println!("New Value of the sample variable is {}", sample_var);

    println!("===== \n");
}
